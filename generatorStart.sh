#!/bin/bash

# i2cset -y 1 0x12 0x04 0x00      BATTERY ISOLATION RELAY
# i2cset -y 1 0x10 0x04 0x00      RUN WIRE
# i2cset -y 1 0x10 0x03 0x00      PRIME WIRE
# i2cset -y 1 0x10 0x02 0x00      START KEY POS
# i2cset -y 1 0x10 0x01 0x00      START REAR WIRE

# isolate my battery
i2cset -y 1 0x12 0x04 0xFF
sleep 1

# put generator in run position
i2cset -y 1 0x10 0x04 0xFF
sleep 1

# prime for 3 seconds
i2cset -y 1 0x10 0x03 0xFF
sleep 3

# activate starter for 1 second
i2cset -y 1 0x10 0x02 0xFF
i2cset -y 1 0x10 0x01 0xFF
sleep 3

# turn off starter wires and primer
i2cset -y 1 0x10 0x03 0x00
#i2cset -y 1 0x10 0x02 0x00
i2cset -y 1 0x10 0x01 0x00

# un-isolate my battery so it can charge
i2cset -y 1 0x12 0x04 0x00

